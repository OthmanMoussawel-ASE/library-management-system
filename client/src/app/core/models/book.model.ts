export interface Book {
  id: string;
  title: string;
  isbn: string | null;
  description: string | null;
  coverImageUrl: string | null;
  totalCopies: number;
  availableCopies: number;
  isAvailable: boolean;
  publishedDate: string | null;
  publisher: string | null;
  pageCount: number | null;
  language: string | null;
  authorId: string;
  authorName: string;
  categories: string[];
  createdAt: string;
}

